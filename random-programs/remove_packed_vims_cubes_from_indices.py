def remove_packed_cubes(filename):

    f = open(filename)
    recs = f.readlines()
    f.close()

    deletions = []
    for name in PACKED_CUBES:
        for k,rec in enumerate(recs):
            if name + '.' in rec and name + '_' not in rec:
                deletions.append(k)

    deleted = []
    for k in deletions[::-1]:       # must be in reverse order!
        deleted.append(recs[k])
        del recs[k]

    f = open(filename, 'w')
    for rec in recs:
        f.write(rec[:-1] + '\r\n')

    f.close()

    return deleted[::-1]
